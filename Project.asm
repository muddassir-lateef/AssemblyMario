playerJump proc
push ax
	mov ax,player.isJumping
	.if ax!=player.jumpheight
		
		.if player.jd==0
			call clearPlayer
			push ax
				mov ax,player.jumpspeed
				.if ax <= decceleration
					mov ax,3
				.else
					sub ax,decceleration
					mov player.jumpspeed,ax
				.endif
			sub player.info.y,ax
			pop ax
			inc player.isJumping
		.else
			call clearPlayer
			push ax
			mov ax,player.jumpspeed
			add ax,acceleration
			mov player.jumpspeed,ax
			add player.info.y,ax

			verticalCollision player,car1,8
			verticalCollision player,car2,8
			verticalCollision player,oil,8

			.if player.info.y>=162
				add landdelay,1
				.if landdelay==5
					mov landdelay,0
					mov player.isJumping,0
					mov player.jd,0
				.endif
				mov player.info.y,162
			.endif
			pop ax

		.endif
	.else
		mov player.jd,1
		inc player.isJumping
		mov player.jumpspeed,0
	.endif

pop ax

	ret
playerJump endp


